package br.com.barcadero.module.sat.devices.integrador.vfpe;

public class VFPeVerificarStatusValidadorDadosParaEnviar {

	private String chaveAcessoValidador;
	private String idFila;
	private String cnpj;
	private String valorIdentificador;
	public String getChaveAcessoValidador() {
		return chaveAcessoValidador;
	}
	public void setChaveAcessoValidador(String chaveAcessoValidador) {
		this.chaveAcessoValidador = chaveAcessoValidador;
	}
	public String getIdFila() {
		return idFila;
	}
	public void setIdFila(String idFila) {
		this.idFila = idFila;
	}
	public String getCnpj() {
		return cnpj;
	}
	public void setCnpj(String cnpj) {
		this.cnpj = cnpj;
	}
	public String getValorIdentificador() {
		return valorIdentificador;
	}
	public void setValorIdentificador(String valorIdentificador) {
		this.valorIdentificador = valorIdentificador;
	}
	
	
}
