package br.com.barcadero.module.sat.handle;



public class HandleLogSAT
{
/**Fields*/
/**Attributes*/
public String dataHora;
public String processo;
public String erroInfo;
public String detalhamento;





public HandleLogSAT () {
	
}


public String getDataHora() {
	return dataHora;
}
public void setDataHora(String dataHora) {
	this.dataHora = dataHora;
}
public String getProcesso() {
	return processo;
}
public void setProcesso(String processo) {
	this.processo = processo;
}
public String getErroInfo() {
	return erroInfo;
}
public void setErroInfo(String erroInfo) {
	this.erroInfo = erroInfo;
}
public String getDetalhamento() {
	return detalhamento;
}
public void setDetalhamento(String detalhamento) {
	this.detalhamento = detalhamento;
}



} /** End Class */
